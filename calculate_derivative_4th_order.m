function dudx = calculate_derivative_4th_order(u, dx)
% CALCULATE_DERIVATIVE_4TH_ORDER 使用四阶中心差分计算导数。
%
%   输入:
%       u  - 因变量值的向量 (至少需要5个点)。
%       dx - 一个标量，表示固定的步长。
%
%   输出:
%       dudx - u 的导数的近似值向量。
%
%   方法:
%       - 内部点 (i=3 to n-2): 四阶中心差分。
%       - 边界附近点: 使用特殊的高阶单侧差分以保持精度。

if ~isscalar(dx) || dx <= 0
    error('步长 dx 必须是一个正标量。');
end
if ~isvector(u) || length(u) < 5
    error('对于四阶方法，输入 u 必须至少包含5个点。');
end

n = length(u);
dudx = zeros(size(u));

% --- 边界点 (使用三阶或四阶单侧差分) ---
% 点 1 (四阶前向差分)
dudx(1) = (-25*u(1) + 48*u(2) - 36*u(3) + 16*u(4) - 3*u(5)) / (12 * dx);

% 点 2 (三阶前向差分)
dudx(2) = (-3*u(2) - 2*u(1) + 6*u(3) - u(4)) / (6*dx); % 这是一个适用于点2的公式

% --- 内部点 (使用四阶中心差分 - 向量化) ---
i = 3:(n-2);
dudx(i) = (-u(i+2) + 8*u(i+1) - 8*u(i-1) + u(i-2)) / (12 * dx);

% --- 边界点 (对称的后向差分) ---
% 点 n-1 (三阶后向差分)
dudx(n-1) = (3*u(n-1) + 2*u(n) - 6*u(n-2) + u(n-3)) / (6*dx);

% 点 n (四阶后向差分)
dudx(n) = (25*u(n) - 48*u(n-1) + 36*u(n-2) - 16*u(n-3) + 3*u(n-4)) / (12 * dx);

end