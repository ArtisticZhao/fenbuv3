# 矩阵形状: [时间步数, 空间点数]
solution_matrix = np.zeros((steps, Nx))
solution_matrix[0, :] = u.value # 存入初始状态
# 初始化热力图对象 (类似于 imagesc 或 surf view 2)
# extent 参数用于设置坐标轴的真实物理范围 [x_min, x_max, t_max, t_min]
