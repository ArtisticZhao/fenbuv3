d1  = 0.3        # diffusion coefficient for Laplacian  (d_1)
d2  = 1.5# -2       # coefficient in the non-local flux     (d_2)
r1  = 0.1           # logistic reaction factor             (f(u)=r1*u*(1-u))
tau =0.002#delay length
L   = np.pi         # domain length
n_init = 2        # k in main_hopf2
u_bar = 1

# grid settings
Nx = 101
dt = 0.0005
Tend = 5  # tital simulation time

# plot
update_step = 200
# save
mat_filename = 'result'

steps = int(round(Tend/dt))

# grid def and initialize
mesh = Grid1D(nx=Nx, Lx=L)

# u: 当前时刻的解 (待求变量)
u = CellVariable(name="u", mesh=mesh, value=0., hasOld=True)

# u_tau: 历史积分后的场 (作为已知系数)
u_tau = CellVariable(name="u_tau", mesh=mesh, value=0.)

# 初始化 u (高斯分布)
x = mesh.cellCenters[0]
#u.setValue(u_bar + 0.02 * np.cos(n_init*np.pi*x / L))
u.setValue(1 + 0.02 * np.cos(n_init*np.pi*x / L))
u.updateOld()

# 可视化初始条件
plt.plot(x, u)
plt.title("u0")

# === 3. 历史数据管理 (环形缓冲区) ===
# 计算需要存储多少步历史
history_steps = int(np.round(2 * tau / dt))
if history_steps % 2 == 0: history_steps += 1

# 初始化历史 buffer — 用预分配的 numpy 环形缓冲区代替 deque
# 避免每步 np.array(deque) 的拷贝开销
u_history_buf = np.zeros((history_steps, Nx))
for i in range(history_steps):
    u_history_buf[i, :] = u.value.copy()
buf_idx = 0  # 环形缓冲区写指针

# 预计算权重 h(s) (同 MATLAB 逻辑)
s_vec = np.linspace(-2*tau, 0, history_steps)
h_weights = np.zeros(history_steps)
for i, s in enumerate(s_vec):
    if -2*tau <= s < -tau:
        h_weights[i] = (1/tau**2)*s + (2/tau)
    elif -tau <= s <= 0:
        h_weights[i] = -(1/tau**2)*s
# 归一化权重 (关键！)
h_weights = h_weights / np.sum(h_weights)

# 预计算环形缓冲区的权重索引顺序（从最老到最新）
# 这样每步只需要根据 buf_idx 重排权重，而不是重建数组
h_weights_buf = h_weights.copy()  # 会在循环内按 buf_idx 重排

# 可视化权重
plt.plot(s_vec, h_weights)
print(f"sum of h_win= {np.sum(h_weights)}")

# 对流项采用迎风格式 d_2 \nabla \cdot ( u \nabla  u_\tau)
velocity = d2 * u_tau.faceGrad
advection = UpwindConvectionTerm(coeff=velocity, var=u)
# 反应项，部分隐式法 reaction = r1 * u * (1-u)
reaction_explicit = r1 * u
reaction_implicit = ImplicitSourceTerm(coeff=-r1*u, var=u)
reaction = reaction_explicit + reaction_implicit
# PDE, 隐式方法
eq = (TransientTerm(var=u) == DiffusionTerm(coeff=d1, var=u) + advection + reaction)

# === 5. 仿真循环 ===

fig, (ax1, ax2, ax3) = plt.subplots(1, 3, figsize=(14, 6))
